package extra01;

import java.util.Random;

/* Crie um programa que gere 10 números aleatórios,
 *  o método main() deve chamar a classe Aleatorio 
 *  para que ela gere tais valores.
 */

public class Atividade07 {
	public static void main(String... args){
		int c = 0;
		
		System.out.println("Gerar 10 valores aleatorios");	
		
		Random aleatorio = new Random();
		for (int x = 1; x <= 10; ++x){
			c++;
			System.out.println(c+ "º Número : " + aleatorio.nextInt(100));
		}
	}
}

